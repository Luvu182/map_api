# Configuration (adjust as needed)
DELAY_SECONDS=3      # Delay between opening URLs
BATCH_SIZE=15        # Number of tabs to open before longer pause
LONG_PAUSE=60        # Longer pause after each batch (time to save files)
DOWNLOAD_DIR="$HOME/Downloads/TIGER_Roads"

# Create download directory
mkdir -p "$DOWNLOAD_DIR"

# Counters
count=0
batch_count=0
total=325

echo -e "${GREEN}=== TIGER Roads Browser Download Script ===${NC}"
echo -e "${BLUE}Total files to download: $total${NC}"
echo -e "${YELLOW}Downloads will open in your default browser${NC}"
echo -e "${YELLOW}Delay between URLs: ${DELAY_SECONDS} seconds${NC}"
echo -e "${YELLOW}Batch size: ${BATCH_SIZE} URLs${NC}"
