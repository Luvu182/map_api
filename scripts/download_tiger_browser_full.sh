echo ""
echo -e "${RED}IMPORTANT: Move downloaded files to: $DOWNLOAD_DIR${NC}"
echo -e "${RED}Press Ctrl+C to stop at any time${NC}"
echo ""
read -p "Press Enter to start downloading..."
echo ""

# Function to open URL
open_url() {
    local url=$1
    local desc=$2
    local filename=$(basename "$url")
    
    count=$((count + 1))
    batch_count=$((batch_count + 1))
    
    echo -e "${GREEN}[$count/$total]${NC} $desc"
    echo -e "  ${BLUE}→ $filename${NC}"
    
    # Open in default browser
    if [[ "$OSTYPE" == "darwin"* ]]; then
        # macOS
        open "$url"
    elif [[ "$OSTYPE" == "linux-gnu"* ]]; then
        # Linux
        xdg-open "$url" 2>/dev/null
    elif [[ "$OSTYPE" == "msys" || "$OSTYPE" == "win32" ]]; then
        # Windows
        start "$url"
    else
        echo -e "${RED}Unknown OS type: $OSTYPE${NC}"
        exit 1
    fi
    
    # Check if batch completed
    if [ $batch_count -eq $BATCH_SIZE ]; then
        echo ""
        echo -e "${YELLOW}=====================================${NC}"
        echo -e "${YELLOW}Completed batch of $BATCH_SIZE downloads${NC}"
        echo -e "${YELLOW}Please move downloaded files to: $DOWNLOAD_DIR${NC}"
        echo -e "${YELLOW}Pausing for $LONG_PAUSE seconds...${NC}"
        echo -e "${YELLOW}=====================================${NC}"
        
        # Countdown
        for ((i=$LONG_PAUSE; i>0; i--)); do
            echo -ne "\r${YELLOW}Resuming in $i seconds...${NC} "
            sleep 1
        done
        echo -e "\r${GREEN}Continuing...              ${NC}"
        echo ""
        
        batch_count=0
    else
        sleep $DELAY_SECONDS
    fi
}

